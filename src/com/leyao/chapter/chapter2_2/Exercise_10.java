public class Exercise_10 {
    public static void main(String[] args) {
        String[] a = {"E", "E", "G", "M", "R", "A", "C", "E", "R", "T"};
        String[] aux = new String[a.length];
        int lo = 0;
        int hi = a.length - 1;
        int mid = (lo + hi) / 2;
        Merge.fastMerge(a, aux, lo, mid, hi);
        System.out.println(Arrays.toString(a));
    }
}
